// lendo a variável que armazena o primeiro valor
let numero1;
//lendo a variável que armazena o segundo valor
let numero2;

// cada uma das duas variáveis criadas recebe um valor específico
numero1 = 15;
// nosso primeiro número recebe o valor 15 e o segundo número recebe o valor 20
numero2 = 20;

// criamos uma terceira variável para armazenar o valor da soma dos dois valores anteriormente atribuidos
let resultado = numero1 + numero2;

// imprimimos essa variável com o resultado da soma na tela
console.log(resultado);